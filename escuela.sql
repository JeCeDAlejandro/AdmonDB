CREATE DATABASE ESCUELA;

USE ESCUELA;

CREATE TABLE PROFESOR_BASE (
	RFC VARCHAR (30) NOT NULL PRIMARY KEY,
	NOM_PROF VARCHAR (30),
	ACADEMIA VARCHAR(30)
);

CREATE TABLE ALUMNO(
	BOLETA INT NOT NULL PRIMARY KEY,
	NOM_ALUMNO VARCHAR (50),
	EDAD INT
);

CREATE TABLE MATERIA(
	CVE_MATERI VARCHAR (5) NOT NULL PRIMARY KEY,
	DESCRICION VARCHAR (50),
	CREDITOS INT
);

CREATE TABLE GRUPO(
	ID_GRUPO INT NOT NULL PRIMARY KEY,
	SECUENCIA VARCHAR (4),
	CVE_MATERIA VARCHAR (10),
	CVE_PROFESOR VARCHAR (15),
	SALON VARCHAR (10)
);

CREATE TABLE ALUMNO_GRUPO(
	BOLETA INT NOT NULL,
	GRUPO INT NOT NULL
);

ALTER TABLE ALUMNO_GRUPO
	ADD PRIMARY KEY (BOLETA, GRUPO);

CREATE TABLE PROFESOR_INTERNO (
	RFC VARCHAR (30) NOT NULL PRIMARY KEY,
	NOM_PROF VARCHAR (30),
	ACADEMIA VARCHAR(30)
);

CREATE TABLE CARRERA(
	CVE_CARRERA VARCHAR (2),
	DESCRICION VARCHAR (20),
	ESCUELA VARCHAR(20)
);

